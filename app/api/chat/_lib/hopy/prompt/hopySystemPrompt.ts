export function getHopySystemPrompt(
  input: HopySystemPromptInput = {},
): string {
  const uiLang = normalizeUiLang(input.uiLang);
  return uiLang === "en" ? buildEnglishPrompt() : buildJapanesePrompt();
}

export default getHopySystemPrompt;